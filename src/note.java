public class note {
    private int id;
    private String name;
    private String body;

    public note() {
    }

    public int id() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String name() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String body() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
